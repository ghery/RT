#include "../lib/rt.h"

double	ft_rgbcmp(int ca, int cb)
{
	int		a[3];
	int		b[3];

	a[0] = ca / 0x10000;
	a[1] = ca % 0x10000 / 0x100;
	a[2] = ca % 0x100;
	b[0] = cb / 0x10000;
	b[1] = cb % 0x10000 / 0x100;
	b[2] = cb % 0x100;
	return ((double)((255 - abs(a[0] - b[0])) * (255 - abs(a[1] - b[1])) *\
			(255 - abs(a[2] - b[2]))) / 16581375);
}
