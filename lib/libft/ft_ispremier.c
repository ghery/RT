#include "libft.h"

int		ft_ispremier(int c)
{
	int		index;

	index = c;
	if (c == 0 || c == 1)
		return (0);
	while (index != 2)
	{
		index--;
		if (c % index == 0)
			return (0);
	}
	return (1);
}
